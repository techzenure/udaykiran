package Day2;
public class CharacterTypes {

	public static void main(String[] args) {
		char c = 'A';
		System.out.println(c);
		
		c = 65; //implicit typecasting
		System.out.println(c);
		
		c = '\u0041';
		System.out.println(c);
		
		c = '\u0c05';
		System.out.println(c);

	}

}
